# typecasting in python

# typecasting is a process of converting a data type into another data type
#there two types of typecasting 
# 1. implicit typecasting
# 2. explicit typecasting

# implicit typecasting
# in implicit typecasting python automatically converts one data type into another data type

# a=2
# b=2.0
# c=a+b
# print(c) # here python automatically converts the integer into float

# # explicit typecasting
# # in explicit typecasting we have to convert the data type by ourself
